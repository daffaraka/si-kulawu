         $(document).ready(function() {
            $('.btnAddTochart').click(function(e) {
                var id = $(this).data('id');
                var qty = 1;
                e.preventDefault();


                $.ajax({
                    url: '/add-to-cart/' + id,
                    type: 'POST',
                    data: {
                        "_token": "{{ csrf_token() }}",
                        "id": id,
                        "qty": qty

                    },
                    success: function(response) {
                        Swal.fire({
                            icon: response.status,
                            title: response.message,
                            text: 'Berhasil ditambahkan',
                            showConfirmButton: false,
                            timer: 1500
                        });
                    }
                });
            });
        });
